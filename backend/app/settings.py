def setup_logging():
    """Configure logging with proper error handling and rotation."""
    logs_dir = BASE_DIR / 'logs'
    
    try:
        # Cr√©er le dossier logs avec les bonnes permissions
        logs_dir.mkdir(exist_ok=True)
        # S'assurer que le dossier est accessible en √©criture
        os.chmod(logs_dir, 0o777)
    except (PermissionError, OSError) as e:
        # Fallback to console-only logging if we can't create log directory
        print(f"‚ö†Ô∏è  WARNING: Cannot create logs directory at {logs_dir}: {e}. Using console logging only.")
        return {
            'version': 1,
            'disable_existing_loggers': False,
            'formatters': {
                'simple': {
                    'format': '{levelname} {asctime} {name} {message}',
                    'style': '{',
                },
            },
            'handlers': {
                'console': {
                    'level': 'DEBUG' if DEBUG else 'INFO',
                    'class': 'logging.StreamHandler',
                    'formatter': 'simple',
                },
            },
            'root': {
                'handlers': ['console'],
                'level': 'DEBUG' if DEBUG else 'INFO',
            },
        }
    
    return {
        'version': 1,
        'disable_existing_loggers': False,
        'formatters': {
            'verbose': {
                'format': '{levelname} {asctime} {module} {process:d} {thread:d} {message}',
                'style': '{',
            },
            'simple': {
                'format': '{levelname} {asctime} {name} {message}',
                'style': '{',
            },
        },
        'handlers': {
            # Handler principal avec rotation par taille
            'file': {
                'level': 'INFO',
                'class': 'logging.handlers.RotatingFileHandler',
                'filename': logs_dir / 'app.log',
                'formatter': 'verbose',
                'maxBytes': 10 * 1024 * 1024,  # 10 MB par fichier
                'backupCount': 5,  # Garde 5 fichiers de sauvegarde
                'encoding': 'utf-8',
            },
            # Handler de s√©curit√© avec rotation quotidienne
            'security': {
                'level': 'INFO',
                'class': 'logging.handlers.TimedRotatingFileHandler',
                'filename': logs_dir / 'security.log',
                'formatter': 'verbose',
                'when': 'midnight',  # Rotation √† minuit
                'interval': 1,  # Chaque jour
                'backupCount': 30,  # Garde 30 jours
                'encoding': 'utf-8',
            },
            # Console pour le d√©veloppement
            'console': {
                'level': 'DEBUG' if DEBUG else 'INFO',
                'class': 'logging.StreamHandler',
                'formatter': 'simple',
            },
            # Handler pour les erreurs critiques (plus petit, plus longue r√©tention)
            'critical': {
                'level': 'ERROR',
                'class': 'logging.handlers.RotatingFileHandler',
                'filename': logs_dir / 'critical.log',
                'formatter': 'verbose',
                'maxBytes': 5 * 1024 * 1024,  # 5 MB par fichier
                'backupCount': 10,  # Garde plus d'historique pour les erreurs
                'encoding': 'utf-8',
            },
            # Handler pour les performances et m√©triques
            'performance': {
                'level': 'INFO',
                'class': 'logging.handlers.TimedRotatingFileHandler',
                'filename': logs_dir / 'performance.log',
                'formatter': 'verbose',
                'when': 'midnight',
                'interval': 1,
                'backupCount': 7,  # Seulement 7 jours pour les perfs
                'encoding': 'utf-8',
            },
        },
        'loggers': {
            'django': {
                'handlers': ['file', 'console', 'critical'],
                'level': 'INFO',
                'propagate': True,
            },
            'django.security': {
                'handlers': ['security', 'critical'],
                'level': 'INFO',
                'propagate': False,
            },
            'authentication': {
                'handlers': ['file', 'security', 'critical'],
                'level': 'INFO',
                'propagate': False,
            },
            'backup_manager': {
                'handlers': ['file', 'performance'],
                'level': 'INFO',
                'propagate': False,
            },
            'database': {
                'handlers': ['file', 'performance'],
                'level': 'INFO',
                'propagate': False,
            },
        },
    }

